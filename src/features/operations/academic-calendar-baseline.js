function text(value) {
  return String(value || "").trim();
}

function normalizeSchool(row = {}) {
  return {
    id: text(row.id),
    name: text(row.name),
    category: text(row.category) || "high",
    color: text(row.color) || "#2f6f63",
    sort_order:
      typeof row.sort_order === "number"
        ? row.sort_order
        : typeof row.sortOrder === "number"
          ? row.sortOrder
          : 0,
    textbooks:
      row.textbooks && typeof row.textbooks === "object" ? row.textbooks : {},
  };
}

function normalizeEvent(row = {}) {
  return {
    id: text(row.id),
    title: text(row.title),
    school_id: text(row.school_id || row.schoolId),
    school: text(row.school),
    type: text(row.type) || "기타",
    start: text(row.start || row.start_date || row.date),
    end: text(row.end || row.end_date || row.start || row.start_date || row.date),
    color: text(row.color) || null,
    grade: text(row.grade) || "all",
    note: text(row.note) || null,
  };
}

export function getAcademicCalendarBaselineSnapshot() {
  return {
    academicCalendarSource: "seed",
    academicSchools: BASELINE_SCHOOLS.map(normalizeSchool),
    academicEvents: BASELINE_EVENTS.map(normalizeEvent),
  };
}

export function resolveAcademicCalendarCollections({
  academicEvents = [],
  academicSchools = [],
  allowSeed = true,
} = {}) {
  if (academicEvents.length > 0 || academicSchools.length > 0) {
    return {
      academicCalendarSource: "live",
      academicEvents,
      academicSchools,
    };
  }

  if (!allowSeed) {
    return {
      academicCalendarSource: "live",
      academicEvents: [],
      academicSchools: [],
    };
  }

  return getAcademicCalendarBaselineSnapshot();
}
